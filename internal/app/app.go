var errFileNotFound = errors.New("file not found")

func TargetFile(filename string) (string, error) {
	var err error

	if filename == "" {
		filename = "."
	}

	info, err := os.Stat(filename)
	if err == nil && info.IsDir() {
		readme, err := findReadme(filename)
		if err != nil {
			return "", err
		}

		filename = readme
	}

	if err != nil {
		err = fmt.Errorf("%w: %s", errFileNotFound, filename)
	}

	return filename, err
}

func ToHTML(markdown string, isMarkdownMode bool) (string, error) {
	var extensions goldmark.Option
	if isMarkdownMode {
		extensions = goldmark.WithExtensions()
	} else {
		extensions = goldmark.WithExtensions(
			&alerts.GhAlerts{Icons: alertIconMap},
			&anchor.Extender{
				Texter: anchor.Text(anchorIcon),
				Unsafe: true, // anchorIcon is a <svg> and needs to be added unescaped
			},
			emoji.Emoji,
			extension.GFM,
			highlighting.NewHighlighting(
				highlighting.WithFormatOptions(
					chromahtml.WithClasses(true),
				),
			),
		)
	}

	md := goldmark.New(
		extensions,
		goldmark.WithParserOptions(parser.WithAutoHeadingID()),
		goldmark.WithRendererOptions(html.WithUnsafe()),
	)

	var buf bytes.Buffer

	err := md.Convert([]byte(markdown), &buf)
	if err != nil {
		return "", fmt.Errorf("markdown convert error: %w", err)
	}

	return buf.String(), nil
}

func Slurp(fileName string) (string, error) {
	f, err := os.Open(fileName)
	if err != nil {
		return "", fmt.Errorf("OS open error: %w", err)
	}

	defer f.Close()
	b, _ := io.ReadAll(f)
	text := string(b)

	return text, nil
}

func findReadme(dir string) (string, error) {
	files, err := os.ReadDir(dir)
	if err != nil {
		// Not returning since read dir will return the files
		// that it read until the error
		utils.LogDebug("Debug [read dir error]: %v", err)
	}

	for _, f := range files {
		r := regexp.MustCompile(`(?i)^readme`)
		if r.MatchString(f.Name()) {
			return filepath.Join(dir, f.Name()), nil
		}
	}

	err = fmt.Errorf("%w: README file not found in %s/", errFileNotFound, dir)

	return "", err
}
